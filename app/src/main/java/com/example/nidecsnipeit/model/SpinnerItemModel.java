package com.example.nidecsnipeit.model;

public class SpinnerItemModel {
    private String id;
    private String name;

    public SpinnerItemModel(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
